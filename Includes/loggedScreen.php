                   <h2>Dans le modèle DDD:</h2>
                   <span>Il n'y a pas de cycle à l'exercice démocratique: c'est un processus continu.
                       Les partis sont remplacés par des structures plus nombreuses et plus souples, les délégantes.
                       Des champs de compétence sont définis et servent de territoires bien segmentés de l'exercice démocratique.</span>
                   <h2>Acteurs de la DDD</h2>

                   <h3>L'électeur</h3>
<span>Comme dans le modèle classique, l'électeur prononce des choix. Ces choix sont structurés différemment, ils peuvent être très simples ou relativement complexes, mais le principe démocratique général reste de mise: les élus sont in fine désignés par lui, l'électeur, et ses pairs.</span>
<h3>Le candidat</h3>
<span>Tout électeur peut se déclarer candidat dans un champ de compétence donné. Il lui appartient ensuite de faire connaître son programme, ses choix et convictions propres.</span>
<h3>Le collège de gestion</h3>
<span>Chaque champ de compétence possède son organe dirigeant, qui est un collège de gestion constitué d'un certain nombre de candidats désignés suivant les mécanismes décrits plus loin. L'un de ces membres devient le secrétaire général du collège.</span>
<h3>La délégante</h3>
<span>La structure délégante - ou plus simplement 'délégante' - est vaguement semblable au parti de la démocratie actuelle, en ce que comme lui elle regroupe les intérêts d'électeurs. Cependant les délégantes diffèrent sur divers points. Les délégantes peuvent agir en chaîne: une délégante peut se prononcer en faveur d'une autre délégante, et de ce point de vue, elle devient une forme de lobby. Les délégantes peuvent être spécialisées dans un ou plusieurs champs de compétence, et leur pouvoir se segmente selon ceux-ci. La direction, la gestion et le financement d'une délégante s'organisent suivant des règles qui lui sont propres. Il suffit qu'elles soient dirigées dans les faits par des électeurs.
Les délégantes peuvent être extrêmement petites, et peuvent se résumer à une simple personne, qui doit être un électeur.</span>
<h3>La base de donnée de délégation</h3>
<span>La logistique du système électoral DDD tourne autour d'une base de données, qui peut être constamment mise à jour. Cette base de données contient tous les choix des électeurs et des délégantes. Elle est accessible en ligne en permanence.</span>
